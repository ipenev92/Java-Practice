package com.ivanp;

public class Info {
    private String color;
    private int number_keys;

    public Info(String color, int number_keys) {
        this.color = color;
        this.number_keys = number_keys;
    }

    public String getColor() {
        return color;
    }

    public int getNumber_keys() {
        return number_keys;
    }
}
